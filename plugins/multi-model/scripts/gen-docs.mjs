#!/usr/bin/env node
/**
 * gen-docs.mjs — regenerate MODELS.md from models.yaml.
 *
 * Usage: node scripts/gen-docs.mjs
 *
 * Reads ../models.yaml and writes ../MODELS.md. No external state. The only
 * runtime dependency is `js-yaml`, resolved via the plugin-local node_modules.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const yaml = require("js-yaml");

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const pluginRoot = path.resolve(__dirname, "..");
const yamlPath = path.join(pluginRoot, "models.yaml");
const outPath = path.join(pluginRoot, "MODELS.md");

const doc = yaml.load(readFileSync(yamlPath, "utf8"));

const SECTIONS = [
  {
    key: "ollama",
    heading: "`mcp__ollama` — Ollama cloud models",
    source: "plugins/multi-model/scripts/mcp-ollama.mjs",
    columns: ["Model", "Provider", "Params", "Ctx", "Why use"],
    row: (m) => [
      `\`${m.id}\``,
      m.provider ?? "",
      m.params ?? "",
      m.ctx ?? "",
      m.notes ?? "",
    ],
  },
  {
    key: "nvidia_nim",
    heading: "`mcp__nvidia-nim` — NVIDIA NIM frontier models",
    source: "plugins/multi-model/scripts/mcp-nvidia.mjs",
    note: "`deepseek-r1` removed (EOL 2026-01-26).",
    columns: ["Alias", "Model ID", "Think", "Why use"],
    row: (m) => [
      `\`${m.id}\``,
      m.provider ?? "",
      m.thinking ? "yes" : "no",
      m.notes ?? "",
    ],
  },
  {
    key: "nvidia_security",
    heading: "`mcp__nvidia-security` — NVIDIA Security NIM models",
    source: "plugins/multi-model/scripts/mcp-security-nvidia.mjs",
    note: "`deepseek-r1` removed (EOL 2026-01-26).",
    columns: ["Alias", "Model ID", "Role", "Why use"],
    row: (m) => [
      `\`${m.id}\``,
      m.provider ?? "",
      m.role ?? "",
      m.notes ?? "",
    ],
  },
  {
    key: "copilot",
    heading: "`mcp__copilot` — GitHub Copilot CLI models",
    source: "plugins/multi-model/scripts/mcp-copilot.mjs",
    columns: ["Model", "Vendor", "Why use"],
    row: (m) => [`\`${m.id}\``, m.provider ?? "", m.notes ?? ""],
  },
];

function renderTable(columns, rows) {
  const head = `| ${columns.join(" | ")} |`;
  const sep = `|${columns.map(() => "---").join("|")}|`;
  const body = rows.map((r) => `| ${r.join(" | ")} |`).join("\n");
  return [head, sep, body].join("\n");
}

const lines = [];
lines.push(
  "<!-- GENERATED FROM models.yaml — do not edit directly. Run: node scripts/gen-docs.mjs -->"
);
lines.push("");
lines.push("# Models");
lines.push("");
lines.push(
  "Single source of truth: `plugins/multi-model/models.yaml`. This file is regenerated by `scripts/gen-docs.mjs`."
);
lines.push("");

const totals = {};
for (const section of SECTIONS) {
  const entries = doc[section.key] ?? [];
  totals[section.key] = entries.length;
  lines.push(`## ${section.heading}`);
  lines.push("");
  lines.push(`Source: [\`${section.source}\`](${section.source})`);
  if (section.note) {
    lines.push("");
    lines.push(`> ${section.note}`);
  }
  lines.push("");
  lines.push(renderTable(section.columns, entries.map(section.row)));
  lines.push("");
}

const grandTotal = Object.values(totals).reduce((a, b) => a + b, 0);
lines.push("---");
lines.push("");
lines.push(
  `**Total: ${grandTotal} models across 4 MCPs** ` +
    `(${totals.ollama} Ollama + ${totals.nvidia_nim} NIM + ${totals.nvidia_security} Security + ${totals.copilot} Copilot).`
);
lines.push("");

// Recommended picks.
lines.push("## Recommended picks");
lines.push("");
lines.push(
  "Curated top 1–2 models per lane. Source: `recommended:` block in `models.yaml`."
);
lines.push("");
lines.push("| Lane | Picks |");
lines.push("|---|---|");
const lanes = doc.recommended ?? {};
for (const [lane, picks] of Object.entries(lanes)) {
  const rendered = picks
    .map((p) => `\`${p.mcp}\` → \`${p.model}\``)
    .join(" · ");
  lines.push(`| \`${lane}\` | ${rendered} |`);
}
lines.push("");

writeFileSync(outPath, lines.join("\n"), "utf8");
console.log(
  `gen-docs: wrote ${path.relative(pluginRoot, outPath)} (${grandTotal} models, ${
    Object.keys(lanes).length
  } lanes)`
);
